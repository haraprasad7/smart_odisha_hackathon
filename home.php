	Group farming can be a game changer in the field of agriculture. 
	Group farming is inspired from the quote "United we stand , divided we fall".
	Group farming in a way is hard to imagine in a country like India where there are numerous elements of diversity.
	In this method a group of farmers with adjacent land near land proximity are clutered together. 
	The groups are then given an unique ID. This ID represents the group. Now a large pathch of land ,
	crop graph can be represented as single unit.
	But what it actually does? Does each farmer in the group get to farm in the whole area?
	Provided it is difficult almost impossible to keep track of personal favours how then shall this be implemented?
	Many more questions of similar type are expected to arise. 
	But this portal with a dummy simple database and a sample process from sowing to selling will demonstrate the implementation.
	One of the major reason behing groups is that of faster reimbursements 
	of  crop insurance claims which has lately been a serious issue in agriculture.
	In case of normal farming there would be 50 distict procedures starting from application of crop insurance to the insurance claim. 
	But in case of group farming , since a large area is now being represented with a single id, 
	the insurance process can come down from 50 procedures to one single procedure. 
	Eventually fastening up the process. This is possible as the natural calamity coverage of crop insurance
	are such that when it affects it affects a large patch of agricultural land and not just a small patch of land.
